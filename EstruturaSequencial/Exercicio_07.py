""" Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário. """
ladoQuadrado = float(input('Informe o valor do lado do quadrado:'))
calculoArea = ladoQuadrado*ladoQuadrado
dobroArea = calculoArea*2
print("A area é {}m² e dobro dela é : {} m²".format(calculoArea,dobroArea))
